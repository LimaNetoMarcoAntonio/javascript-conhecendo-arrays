// Limpa o console antes de executar o programa
console.clear();

// Definindo o array de nomes
const nomes = ['ana Julia', 'Caio vinicius', 'BIA silva'];

// Usando o método map para deixar todos os nomes em letras maiúsculas
const nomesMaiusculo = nomes.map(nome => nome.toUpperCase());

// Exibindo os nomes em letras maiúsculas
console.log(nomesMaiusculo);
